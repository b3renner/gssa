let currentLang = localStorage.getItem('gssa-lang') || 'pt';

function t(key) {
    return TRANSLATIONS[currentLang][key] ?? TRANSLATIONS['pt'][key] ?? key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => { el.innerHTML = t(el.dataset.i18n); });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => { el.placeholder = t(el.dataset.i18nPh); });
    document.querySelectorAll('[data-i18n-text]').forEach(el => { el.textContent = t(el.dataset.i18nText); });
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
        const lbl = btn.querySelector('.lang-label');
        if (lbl) lbl.textContent = t('lang-toggle-label');
        btn.title = currentLang === 'pt' ? 'Switch to English' : 'Mudar para Português';
    });
    document.documentElement.lang = currentLang === 'pt' ? 'pt-BR' : 'en';
}

function toggleLanguage() {
    currentLang = currentLang === 'pt' ? 'en' : 'pt';
    localStorage.setItem('gssa-lang', currentLang);
    applyTranslations();
    document.dispatchEvent(new CustomEvent('gssa-lang-changed', { detail: { lang: currentLang } }));
}

document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => btn.addEventListener('click', toggleLanguage));
    applyTranslations();
});

window.gssaI18n = { t, currentLang: () => currentLang, applyTranslations, toggleLanguage };
